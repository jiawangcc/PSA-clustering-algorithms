public class Clusteredpoint {
  private double x;
  private double y;
  private double orignialid;
  private double newid;

  public void setX(double x) {
    this.x = x;
  }

  public void setY(double y) {
    this.y = y;
  }

  public void setOriginalid(double originalid) {
    this.orignialid = originalid;
  }
 
  public void setNewid(double newid) {
	this.newid = newid;
  }

  public double getX() {
    return x;
  }

  public double getY() {
    return y;
  }

  public double getOriginalid() {
    return orignialid;
  }
  
  public double getNewid() {
	return newid;
  }
  
  public Clusteredpoint() {
  }
}