public class Distance {
  private double xdis;
  private double ydis;
  private double x1;
  private double y1;
  private double x2;
  private double y2;

  public void setxdis(double xdis) {
    this.xdis = xdis;
  }

  public void setydis(double ydis) {
	    this.ydis = ydis;
  }
  
  public void setx1(double x1) {
	    this.x1 = x1;
	  }
  public void sety1(double y1) {
	    this.y1 = y1;
	  }
  public void setx2(double x2) {
	    this.x2 = x2;
	  }
  public void sety2(double y2) {
	    this.y2 = y2;
	  }

  public double getxdis() {
    return xdis;
  }

  public double getydis() {
	    return ydis;
	  }

  public double getx1() {
	    return x1;
	  }

  public double gety1() {
	    return y1;
	  }

  public double getx2() {
	    return x2;
	  }

  public double gety2() {
	    return y2;
	  }

  public Distance() {
  }
}


